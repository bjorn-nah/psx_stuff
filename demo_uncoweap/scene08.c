//local fontions
void init_scene08(){
	
	color_mtx.m[0][0]=ONE * 1/4;
	color_mtx.m[1][0]=ONE * 1/4;
	color_mtx.m[2][0]=ONE * 1/4;
	
	FntLoad(960, 0);
	FntOpen(64, 160, 192, 64, 2, 256);
	
	/* Initialize the GTE */
	InitGeom();
	
	/* Set GTE offset (recommended method  of centering) */
	gte_SetGeomOffset( CENTERX, CENTERY );
	
	/* Set screen depth (basically FOV control, W/2 works best) */
	gte_SetGeomScreen( CENTERX );
	
	/* Set light ambient color and light color matrix */
	gte_SetBackColor( 63, 63, 63 );
	gte_SetColorMatrix( &color_mtx );
	
	nb_faces = sizeof(deathstarmini_norms) / sizeof(deathstarmini_norms[0]);
	curent_color = 1;
}

void scene08_play(RenderContext ctx){
	
	int i,p;
	
	int time = 0;
	
	int direction_x = 0;
	int direction_z = 0;

	setup_context(&ctx, SCREEN_XRES, SCREEN_YRES, 10, 10, 10);

	init_scene08();
	
	SVECTOR	rot = { 0 };			/* Rotation vector for Rotmatrix */
	VECTOR	pos = { -40, 0, 150 };
	MATRIX	mtx,lmtx;				/* Rotation matrices for geometry and lighting */
	
	rot.vx = 2048;
	
	POLY_F3 *pol3;
	
	/* Main loop */
	for(;;) {
		
		time++;
		
		/* Set rotation and translation to the matrix */
		RotMatrix( &rot, &mtx );
		TransMatrix( &mtx, &pos );
		
		/* Multiply light matrix by rotation matrix so light source */
		/* won't appear relative to the model's rotation */
		MulMatrix0( &light_mtx, &mtx, &lmtx );
		
		/* Set rotation and translation matrix */
		gte_SetRotMatrix( &mtx );
		gte_SetTransMatrix( &mtx );
		
		/* Set light matrix */
		gte_SetLightMatrix( &lmtx );
		
		rot.vy += 16;
		
		light_mtx.m[0][0] = isin(time<<2);
		light_mtx.m[0][1] = icos(time<<2);
		
		if(pos.vx<-80){direction_x =0;}
		if(pos.vx>80){direction_x =1;}
		if(direction_x == 0){
			pos.vx += 1;
		}else{
			pos.vx -= 1;
		}
		
		if(pos.vz<120){direction_z =0;}
		if(pos.vz>180){direction_z =1;}
		if(direction_z == 0){
			pos.vz += 1;
		}else{
			pos.vz -= 1;
		}
		
		pos.vy = 64-abs(isin(time*16)/32);
			

		
		gte_SetColorMatrix( &color_mtx );
		curent_color=1;
		
		
		for(i=0; i<nb_faces;i++){
			
			
			/* Load the first 3 vertices of a quad to the GTE */
			gte_ldv3( 
				&deathstarmini_verts[deathstarmini_indices[i].v0], 
				&deathstarmini_verts[deathstarmini_indices[i].v1], 
				&deathstarmini_verts[deathstarmini_indices[i].v2] );
				
			/* Rotation, Translation and Perspective Triple */
			gte_rtpt();
			
			/* Compute normal clip for backface culling */
			gte_nclip();
			
			/* Get result*/
			gte_stopz( &p );
			
			/* Skip this face if backfaced */
			if( p < 0 )
				continue;
			
			/* Calculate average Z for depth sorting */
			gte_avsz4();
			gte_stotz( &p );
			
			/* Skip if clipping off */
			/* (the shift right operator is to scale the depth precision) */
			if( (p>>2) > OT_LENGTH )
				continue;
			
			pol3 = (POLY_F3 *) new_primitive(&ctx, p, sizeof(POLY_F3));
			
			/* Initialize a quad primitive */
			//setPolyFT4( pol3 );
			setPolyF3(pol3);
			
			/* Set the projected vertices to the primitive */
			gte_stsxy0( &pol3->x0 );
			gte_stsxy1( &pol3->x1 );
			gte_stsxy2( &pol3->x2 );
			
			/* Load primitive color even though gte_ncs() doesn't use it. */
			/* This is so the GTE will output a color result with the */
			/* correct primitive code. */
			gte_ldrgb( &pol3->r0 );
			
			/* Load the face normal */
			gte_ldv0( &deathstarmini_norms[i] );
			
			/* Normal Color Single */
			gte_ncs();
			
			/* Store result to the primitive */
			gte_strgb( &pol3->r0 );
			
		}
		
		FntPrint(-1, "\n\n Unconventional weapons\n");
		FntPrint(-1, " A short story for PSX.\n");
		FntPrint(-1, "\n         THE END\n");
		FntFlush(-1);

		flip_buffers(&ctx);
		//checkMusic();
	}

}